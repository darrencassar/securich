DROP PROCEDURE IF EXISTS show_reserved_usernames;

DELIMITER $$

CREATE PROCEDURE `securich`.`show_reserved_usernames`()
  BEGIN

     select USERNAME as 'reserved usernames' from securich.sec_reserved_usernames;

  END$$

